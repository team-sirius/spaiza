namespace Base;

class User extends \Main{
    protected int $uid;
    
    public function __construct() {
        parent::__construct();
        $this->uid=(int)($_COOKIE[\Cfg::COOKIE_USER]??0);
    }
    
    public function canLink(string $link): bool {
        if(($this->error = \Checker::errInLink($link))) return false;
        $q= $this->query("SELECT id FROM `users` WHERE link='$link'");
        if(!$q) return false;
        if($q->rowCount()!=0){
            $this->error= lnTranslate("This username is already taken");
            return false;
        }
        return true;
    }
}
